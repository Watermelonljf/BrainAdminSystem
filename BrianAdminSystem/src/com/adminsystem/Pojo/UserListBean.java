package com.adminsystem.Pojo;

public class UserListBean {
	private int userId;
	private String strUserName;
	private String strUserSex;
	private String strUserTel;
	private String strUserQ;
	private String strUserEmail;
	private String strAddress;
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getStrUserName() {
		return strUserName;
	}
	public void setStrUserName(String strUserName) {
		this.strUserName = strUserName;
	}
	public String getStrUserSex() {
		return strUserSex;
	}
	public void setStrUserSex(String strUserSex) {
		this.strUserSex = strUserSex;
	}
	public String getStrUserTel() {
		return strUserTel;
	}
	public void setStrUserTel(String strUserTel) {
		this.strUserTel = strUserTel;
	}
	public String getStrUserQ() {
		return strUserQ;
	}
	public void setStrUserQ(String strUserQ) {
		this.strUserQ = strUserQ;
	}
	public String getStrUserEmail() {
		return strUserEmail;
	}
	public void setStrUserEmail(String strUserEmail) {
		this.strUserEmail = strUserEmail;
	}
	public String getStrAddress() {
		return strAddress;
	}
	public void setStrAddress(String strAddress) {
		this.strAddress = strAddress;
	}
}
